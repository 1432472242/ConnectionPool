#pragma once
#include<mysql.h>
#include<string>
#include<ctime>
using namespace std;
/* 实现MySQL数据库的操作*/
class Connection
{
public:
	Connection();//初始化数据库连接
	~Connection();//释放数据库连接资源
	bool connect(string ip,//ip地址
		unsigned short port,//端口号
		string username,//用户名
		string password,//密码
		string dbname);//选择的数据库
	bool update(string sql);//更新操作 insert delete  update
	MYSQL_RES* query(string sql);//查询操作 select
	
    //刷新一下连接的起始空闲时间点
	void refreshAliveTime() { _alivetime = clock(); }
	//返回存活时间
	clock_t getAliveTime() { return clock() - _alivetime; }

private:
	MYSQL* _conn;//表示和MYSQL  Server的一条连接
	clock_t _alivetime;//记录进入空闲状态后的起始存活时间

};
